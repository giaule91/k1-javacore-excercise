package com.demo.core.lap2;

public class Employee {
	public String name;
	public int age;
	public String job;
	public double salary;
	public String department;
	public Employee(String name, int age, String job, double salary, String department) {
		this.name=name;
		this.age=age;
		this.job=job;
		this.salary=salary;
		this.department=department;
	}
}
